package src.Enum;

/**
 * The {@code ServiceType} enum represents different types of medical services
 * offered in the hospital management system.
 *
 * <p>
 * <b>Enum Constants:</b></p>
 * <ul>
 * <li>{@link #CONSULTATION} - Represents a standard medical consultation
 * service.</li>
 * <li>{@link #XRAY} - Represents an X-ray imaging service.</li>
 * <li>{@link #BLOOD_TEST} - Represents a blood test service.</li>
 * </ul>
 *
 * <p>
 * This enum is used to classify and manage various services provided during
 * appointments and treatments.</p>
 *
 * @author Bryan
 * @version 1.0
 * @since 2024-11-17
 */
public enum ServiceType {
    /**
     * Represents a standard medical consultation service.
     */
    CONSULTATION,
    /**
     * Represents an X-ray imaging service.
     */
    XRAY,
    /**
     * Represents a blood test service.
     */
    BLOOD_TEST;
}
